int main(void){
  TExaS_Init(SSI0_Real_Nokia5110_Scope);  // set system clock to 80 MHz
  Random_Init(1);
	Nokia5110_Init();
	GameSound_Init();
	ADC_Init();
	Semaphore=0;
	Systick_Init(2666667); //30Hz
	Reset_Game();
  while(1){
		while(1){
			while(Semaphore==0){}
			Update_LCD();
			if((Player.life == 0) || (AllDead==1)) break; 
		}	
		if(Player.life==0){
			Nokia5110_Clear();
			Nokia5110_SetCursor(1, 1);
			Nokia5110_OutString("GAME OVER");
			Nokia5110_SetCursor(1, 2);
			Nokia5110_OutString("Nice try,");
			Nokia5110_SetCursor(1, 3);
			Nokia5110_OutString("Earthling!");
			Nokia5110_SetCursor(2, 4);
		}
		else{
			Nokia5110_SetCursor(1,1);
			Nokia5110_OutString("Well done!");
			Nokia5110_SetCursor(1,2);
			Nokia5110_OutString("You won.");
		}
		while((Switch_Read() & 0x3) == 0){}
		Nokia5110_Clear();
		Delay100ms(5);
		Reset_Game();
	}
}
void Delay100ms(unsigned long count){unsigned long volatile time;
  while(count>0){
    time = 727240;  // 0.1sec at 80 MHz
    while(time){
	  	time--;
    }
    count--;
  }
}
void Systick_Init(unsigned long period){
	NVIC_ST_CTRL_R = 0x06;
	NVIC_ST_RELOAD_R = period-1;
	NVIC_ST_CURRENT_R = 0x0;
	NVIC_ST_CTRL_R |= 0x01;
}
void SysTick_Handler(void){
	unsigned long Data;
	Data = Switch_Read();
	//read ADC
	ShipPos = ADC_In()/62; //84 pixel, player width:18, 12bit ADC => [0 ; 84-18]: 66-0=66 4096/66 = 62.0606
	if((Data & 0x2)&& (PlayerMissile.life==0)){
		PlayerMissile.x=ShipPos+8;
		PlayerMissile.y=41;
		PlayerMissile.life=1;
		GameSound_Play(227); //play shooting sound
	}
	if((Data & 0x1)&& (PlayerSpecMissile.life==0)){
		//TODO shoot special missile animation
		//PlayerSpecMissile.x=ShipPos+8;
		//PlayerSpecMissile.y=41;
		//PlayerSpecMissile.life=1;
		EnemyAttack();
		GameSound_Play(227); //play shooting sound
	}
	PlayerMissile_Move();
	Player_Move(ShipPos);
	EnemyMissile_Move();
	Enemy_Move();
	//draw to RAM buffer
	Nokia5110_ClearBuffer();
	PlayerMissile_Draw();
	EnemyMissile_Draw();
	Player_Draw();
	Enemy_Draw();
	Bunker_Draw();
	Semaphore=1;
}
unsigned short AnimateEnemy; //TODO redundant? framecount instead?
void Enemy_Init(void){int i;
	for(i=0; i<5;i++){
		Enemy[i].x=(i*16);
		Enemy[i].y=ENEMY10H-1;
		Enemy[i].image=SmallEnemy10PointA;
		Enemy[i].life=2;
	}
	AllDead=0;
	AnimateEnemy=1;
}
void Enemy_Move(void){int i;
	for(i=0; i<5;i++){
		if(Enemy[i].life==2){
			if(AnimateEnemy == 1){
				Enemy[i].x +=2;
			}
			else{
				Enemy[i].x -=2;
			}
		}
	}
	AnimateEnemy ^= 1UL;
}
unsigned short FrameCount=0;
void Enemy_Draw(void){int i; unsigned short dead=0; 
	for(i=0;i<5;i++){
		if(Enemy[i].life==2){
			if(FrameCount == 0){
				Nokia5110_PrintBMP(Enemy[i].x,Enemy[i].y,SmallEnemy10PointA,0);
			}
			else{
				Nokia5110_PrintBMP(Enemy[i].x,Enemy[i].y,SmallEnemy10PointB,0);
			}
		}
		else{
			if(Enemy[i].life==1){
				Nokia5110_PrintBMP(Enemy[i].x,Enemy[i].y,SmallExplosion0,0);
				Enemy[i].life=0;
			}
			else{
				Nokia5110_PrintBMP(Enemy[i].x,Enemy[i].y,SmallExplosion1,0);
				dead++;
			}
		}
	}
	if(dead==5) AllDead=1;
	FrameCount = (FrameCount+1)&0x01;
}
void Player_Init(void){
	Player.x=32;
	Player.y=47;
	Player.image=PlayerShip0;
	Player.life=3;
}
void Player_Move(unsigned long data){
	if(Player.life==3){
		Player.x= data; //84 pixel, player width:18, 12bit ADC => [0 ; 84-18]: 66-0=66 4096/66 = 62.0606
	}
}
void Player_Draw(void){
	if(Player.life==3){
		Nokia5110_PrintBMP(Player.x,Player.y,Player.image,0);
	}
	if(Player.life==4){
		Player.life=0;
	}
	if(Player.life==1){
		Nokia5110_PrintBMP(Player.x,Player.y,BigExplosion1,0);
		Player.life=4;
	}
	if(Player.life==2){
		Nokia5110_PrintBMP(Player.x,Player.y,BigExplosion0,0);
		Player.life=1;
	}
}
